#include<stdio.h>

int main()
{
    int  i=5, valor1=0, valor2=0, valor3=1; //Las variables VALOR tienen que quedar en 24, -10 y -44

                                    //Se modifico el valor de la variable I para utilizarla en el while.

    while(i>0)                      //Se liminaron sentencias y variables por la razon de que son innecesarias.

    {
        valor1=20+4;
        valor2=-20+10;              //Se sigue conservando el valor de I para decrementarse asi mismo asi de esta forma terminar el While.
        valor3=-20-20-4;
        i--;
    }

    printf(" %d %d %d %d ", i, valor1, valor2, valor3); //Muestro por pantalla el valor en que quedaron todas las variables.
    return 0;
}
//La funcion de este programa es sumar y restar numeros a las variables valor para llegar al objetivo final; conseguir que queden en 24, -10 y -44


